def format_pairwise_user_prompt(task: OpenRSPairTask, *, with_skill_hint: bool = False) -> str:
    if is_judgebench(task.benchmark):
        prompt = format_judgebench_prompt(task)
        if with_skill_hint:
            prompt = (
                prompt.rstrip()
                + "\n\nTool-use note for the judge: an optional `use_skill` tool may be available. "
                "The required final textual answer is still Output (a) or Output (b), but this format requirement does not prevent calling the tool first when it would materially improve the judgment."
            )
        return prompt
    if is_rmbench(task.benchmark):
        prompt = format_rmbench_user_prompt(task)
        if with_skill_hint:
            prompt = "You may use the optional reward-judge skill if it would materially improve this judgment.\n\n" + prompt
        return prompt

    parts = [
        "Compare the two responses to the user request.",
        "Choose Response A if A is clearly better, Response B if B is clearly better, or Tie if neither is clearly better.",
        "Do not assume either response is preferred because of its label or position.",
    ]
    if with_skill_hint:
        parts.append("You may use the optional reward-judge skill if it would materially improve this judgment.")
    parts.extend(
        [
            "",
            "[User Request]",
            task.prompt,
            "",
            "[Response A]",
            task.responses["A"],
            "",
            "[Response B]",
            task.responses["B"],
            "",
            "Final answer format: call final_answer or end with exactly one line `Final: A`, `Final: B`, or `Final: Tie`.",
        ]
    )
    return "\n".join(parts)


