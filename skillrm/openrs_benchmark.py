def run_pairwise_position_swap_audit(
    task: OpenRSPairTask,
    base_url: str,
    config: dict[str, Any],
    skill_package: dict[str, Any],
    initial_parsed: dict[str, str],
) -> dict[str, Any]:
    audit_config = dict(config)
    audit_config["max_tokens"] = int(config.get("position_swap_audit_max_tokens", 512))
    audit_config["retries"] = int(config.get("position_swap_audit_retries", config.get("retries", 2)))
    audit_config["enable_thinking"] = bool(config.get("position_swap_enable_thinking", False))
    audit_config["send_thinking_field"] = bool(config.get("position_swap_send_thinking_field", True))
    swapped_task = OpenRSPairTask(
        task_id=f"{task.task_id}::position_swap",
        benchmark=task.benchmark,
        sample_id=task.sample_id,
        prompt=task.prompt,
        responses={"A": task.responses["B"], "B": task.responses["A"]},
        gold_label=task.gold_label,
        query_type=task.query_type,
        domain=task.domain,
        pair=task.pair,
        order=task.order,
        metadata=task.metadata,
        sample_resources=task.sample_resources,
    )
    swapped_response, swapped_parsed = call_position_swap_verdict(
        base_url,
        format_position_swap_direct_messages(swapped_task),
        audit_config,
        task.benchmark,
        forced_instruction=position_swap_forced_instruction(task.benchmark, swapped=True),
    )
    swapped_valid = pairwise_final_valid_for_benchmark(task.benchmark, swapped_parsed)
    mapped_winner = map_swapped_pairwise_winner(swapped_parsed.get("winner")) if swapped_valid else None
    initial_winner = initial_parsed.get("winner")
    audit: dict[str, Any] = {
        "enabled": True,
        "initial_winner": initial_winner,
        "swapped_winner": swapped_parsed.get("winner"),
        "mapped_winner": mapped_winner,
        "swapped_source": swapped_parsed.get("source"),
        "swapped_valid": swapped_valid,
        "consistent": bool(mapped_winner and mapped_winner == initial_winner),
        "override_applied": False,
        "parse_error": None if swapped_valid else "position-swap pass did not return a valid verdict",
        "swapped_raw_output": swapped_response.get("content", ""),
        "swapped_finish_reason": swapped_response.get("finish_reason"),
        "swapped_latency_sec": swapped_response.get("latency_sec"),
    }
    if not swapped_valid or mapped_winner == initial_winner:
        return audit
    if not bool(config.get("position_swap_audit_adjudicate", True)):
        audit["parse_error"] = "position-swap disagreement without adjudication"
        return audit

    adjudicator_response, adjudicator_parsed = call_position_swap_verdict(
        base_url,
        format_position_swap_adjudicator_messages(task, skill_package, initial_parsed, swapped_parsed, mapped_winner),
        audit_config,
        task.benchmark,
        forced_instruction=position_swap_forced_instruction(task.benchmark, swapped=False),
    )
    adjudicator_valid = pairwise_final_valid_for_benchmark(task.benchmark, adjudicator_parsed)
    audit.update(
        {
            "adjudicator_winner": adjudicator_parsed.get("winner"),
            "adjudicator_source": adjudicator_parsed.get("source"),
            "adjudicator_valid": adjudicator_valid,
            "adjudicator_raw_output": adjudicator_response.get("content", ""),
            "adjudicator_finish_reason": adjudicator_response.get("finish_reason"),
            "adjudicator_latency_sec": adjudicator_response.get("latency_sec"),
        }
    )
    if adjudicator_valid:
        adjudicator_parsed = dict(adjudicator_parsed)
        adjudicator_parsed["source"] = f"position_swap_audit.{adjudicator_parsed.get('source')}"
        audit["final_parsed"] = adjudicator_parsed
        audit["raw_output"] = adjudicator_response.get("content", "")
        audit["response"] = {
            "content": adjudicator_response.get("content", ""),
            "finish_reason": adjudicator_response.get("finish_reason"),
            "latency_sec": adjudicator_response.get("latency_sec"),
            "reasoning_len": adjudicator_response.get("reasoning_len", 0),
            "thinking_field_sent": adjudicator_response.get("thinking_field_sent"),
            "error": adjudicator_response.get("error"),
        }
        audit["override_applied"] = adjudicator_parsed.get("winner") != initial_winner
        audit["parse_error"] = None
    else:
        audit["parse_error"] = "position-swap adjudicator did not return a valid verdict"
    return audit


def call_position_swap_verdict(
    base_url: str,
    messages: list[dict[str, Any]],
    config: dict[str, Any],
    benchmark: str,
    *,
    forced_instruction: str,
) -> tuple[dict[str, Any], dict[str, str]]:
    tools = [pairwise_final_answer_tool(benchmark)]
    tool_choice = config.get("position_swap_tool_choice", "required")
    response = call_with_retries(
        base_url,
        messages,
        config,
        tools=tools,
        tool_choice=tool_choice,
    )
    final_tool = first_final_answer_tool_call(response.get("tool_calls") or [])
    parsed = parse_pairwise_final_answer_tool_call(final_tool) if final_tool is not None else parse_pairwise_final(
        str(response.get("content") or ""),
        finish_reason=response.get("finish_reason"),
    )
    if pairwise_final_valid_for_benchmark(benchmark, parsed):
        return response, parsed

    forced_messages = list(messages)
    if response.get("content"):
        forced_messages.append({"role": "assistant", "content": str(response.get("content") or "")})
    forced_messages.append({"role": "user", "content": forced_instruction})
    forced_config = dict(config)
    forced_config["max_tokens"] = int(config.get("position_swap_forced_finalization_max_tokens", 32))
    forced = call_with_retries(base_url, forced_messages, forced_config)
    forced_parsed = parse_pairwise_final(
        str(forced.get("content") or ""),
        finish_reason=forced.get("finish_reason"),
    )
    if pairwise_final_valid_for_benchmark(benchmark, forced_parsed):
        return forced, forced_parsed
    return response, parsed


